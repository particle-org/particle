    private static Map<String, SoundType> allTypes = new ConcurrentHashMap<>();

    static {
        for (SoundType soundType : SoundType.values()) {
            allTypes.put(soundType.name, soundType);
        }
    }

    private String name;

    private String category;

    SoundType(String name, String category) {
        this.name = name;
        this.category = category;
    }

    public String getName() {
        return name;
    }

    public String getCategory() {
        return category;
    }

    public static SoundType fromValue(String name) {
        return allTypes.get(name);
    }
